// Example document for fresh accounts (aggregated into the bundle's init.js):
// a 50×50 canvas with a witch-on-a-broomstick doodle already drawn, instead
// of a blank grid. Standalone: builds the doc shape inline (mirrors
// PatternWitchDatatype.init) instead of going through the plugin registry.

export default async function example(repo) {
  const handle = await repo.create2({
    "@patchwork": {
      type: "patternwitch",
      suggestedImportUrl: new URL("./patternwitch.js", import.meta.url).href,
    },
    title: "PatternWitch",
    width: 50,
    height: 50,
    palette: ["#f4f1de", "#e07a5f", "#3d405b", "#81b29a"],
    pixels: ART.map((row) => [...row].map((ch) => (ch === "." ? 0 : Number(ch)))),
  });

  return { name: "PatternWitch", type: "patternwitch", url: handle.url };
}

